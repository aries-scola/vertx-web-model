import javax.validation.ConstraintViolation;
import javax.validation.Validator;

import org.springframework.beans.BeanUtils;
import org.springframework.beans.BeanWrapper;
import org.springframework.beans.BeanWrapperImpl;
import org.springframework.beans.InvalidPropertyException;

public class Form<T> {

    final static Logger logger = Logger.getLogger(Form.class.getName());

    private boolean readonly = false;
    private Set<String> errors = null;

	public static class PossibleValue {		
		private String label = null;
		private String value = null;
		
		public PossibleValue(String value, String label) {
			Objects.requireNonNull(value);
			Objects.requireNonNull(label);
			
			this.value = value;
			this.label = label;
		}

		public String getLabel() {
			return label;
		}
		
		public String getValue() {
			return value;
		}
	}

	public class Field {		
		private String name = null;
		private String type = null;
		private String value = null;
		private boolean readonly = false;
		private Set<String> errors = null;
		private Collection<PossibleValue> possibleValues = null;
		
		public Field(String name, String type) {
			Objects.requireNonNull(name);
			Objects.requireNonNull(type);

			this.name = name;
			this.type = type;
		}

		public Field addError(String error) {
			if (errors==null) {
				errors = new HashSet<>();
			}
			errors.add(error);
			return this;
		}
		
		public Field addPossibleValue(PossibleValue value) {
			if (possibleValues==null) {
				possibleValues = new ArrayList<PossibleValue>();
			}
			possibleValues.add(value);
			return this;
		}

		/**
		 * return a copy of the field
		 * 
		 */
		@Override
		public Field clone() {
			Field result = new Field(name, type);
			// copy errors
			for (String error: errors()) {
				result.addError(error);
			}
			// copy the value
			result.value = value;
			return result;
		}
		
		public Collection<String> errors() {
			if (errors==null) {
				return Collections.emptyList();
			}
			return errors;
		}

		public boolean hasErrors() {
			return errors!=null && errors.size()>0;
		}

		public String name() {
			return name;
		}

		public Collection<PossibleValue> possibleValues() {
			if (possibleValues==null) {
				return Collections.emptyList();
			}
			return possibleValues;
		}
		
		public String type() {
			return type;
		}

		public Field type(String type) {
			this.type = type;
			return this;
		}
		
		public String value() {
			return value;
		}

		public Field value(String value) {
			this.value = value;
			return this;
		}
		
		public boolean readonly() {
			return Form.this.isReadonly() || readonly;
		}

		public void readonly(boolean readOnly) {
			this.readonly = readOnly;
		}
	}
	
	public Form<T> addError(String error) {
		if (errors==null) {
			errors = new HashSet<>();
		}
		errors.add(error);
		return this;
	}
	
	public Collection<String> errors() {
		if (errors==null) {
			return Collections.emptyList();
		}
		return errors;
	}

	/**
	 * Build a new Form instance from the specified object
	 * 
	 * @param object
	 * @param fieldPrefix: if not null, the prefix is prepended to each field name 
	 * @return
	 */
	public final static <T> Form<T> fromBean(Validator validator, T object, String fieldPrefix, String referer) {
		Objects.requireNonNull(object);
		
		@SuppressWarnings("unchecked")
		Form<T> result = (Form<T>) Form.fromClass(object.getClass(), fieldPrefix);
        result.referer = referer;
		return result.bindObject(validator, object);
	}

	/**
	 * Build a new Form instance from the specified class
	 * 
	 * @param cls
	 * @param fieldPrefix: if not null, the prefix is prepended to each field name 
	 * @return
	 */
	public final static <T> Form<T> fromClass(Class<T> cls, String fieldPrefix) {
		Objects.requireNonNull(cls);
	
		Form<T> result = new Form<T>();
		if (fieldPrefix!=null) {
			result.fieldPrefix = fieldPrefix;
		}

		// discover properties
		for (PropertyDescriptor property: BeanUtils.getPropertyDescriptors(cls)) {
			if (property.getReadMethod()!=null && property.getWriteMethod()!=null) {
			// the property has a getter and setter
				String fieldName = fieldPrefix!=null ? fieldPrefix + property.getName() : property.getName();
				result.addField(result.new Field(fieldName, Form.fieldTypefromClass(property.getPropertyType())));
			}
		}

		return result;
	}
	/**
	 * validate the specified bean, optionally validating only the specified properties
	 * 
	 * @return
	 */
	public final static Set<ConstraintViolation<?>> validate(Validator validator, Object object, String... properties) {
		Set<ConstraintViolation<?>> result = null;
		if (validator!=null && object!=null) {
			if (properties.length>0) {
			// validate only the specified properties
				result = new HashSet<>();
				for (String property: properties) {
					try {
						Set<ConstraintViolation<Object>> constraintViolations = validator.validateProperty(object, property);
						result.addAll(constraintViolations);
					}
					catch (Throwable t) {
                        logger.log(Level.FINE, "Could not validate property ", t);
					}
				}
			}
			else {
			// validate the object
				try {
					Set<ConstraintViolation<Object>> constraintViolations = validator.validate(object);
					result = new HashSet<>();
					result.addAll(constraintViolations);
				}
				catch (Throwable t) {
                    logger.log(Level.FINE, "Could not validate Object ", t);
				}
			}
		}
		return result!=null ? result : Collections.emptySet();
	}

	private T object = null;
	private Map<String, Field> fields = null;
	private String fieldPrefix = null;
	private String referer = null;
	private String url = null;
	
	public Form() {
	}
	
	public Form<T> addField(Field field) {
		Objects.requireNonNull(field);
		Objects.requireNonNull(field.name());
		
		if (fields==null) {
			fields = new LinkedHashMap<>();
		}
		fields.put(field.name(), field);
		return this;
	}

	/**
	 * bind all or only the specified properties of the object to this Form. 
	 * Validation is automatically performed after the binding, validating all or only the specified properties. 
	 * The result of the validation is available by calling form.hasErrors() and/or field.hasErrors() 
	 * 
	 * @param object
	 * @param properties
	 * @return
	 */
	public Form<T> bindObject(Validator validator, T object, String... properties) {
		if (object!=null) {
			this.object = object;
			int boundPropertyCount = 0; 
			BeanWrapper beanWrapper = new BeanWrapperImpl(object);
			for (Field field: fields()) {
				// if applicable, remove the fieldPrefix to get the bean field name 
				String fieldName = fieldPrefix!=null ? field.name().substring(fieldPrefix.length()) : field.name();
				PropertyDescriptor property = beanWrapper.getPropertyDescriptor(fieldName);
				if (property!=null && property.getReadMethod()!=null) {
					boundPropertyCount++;
					Object propertyValue = beanWrapper.getPropertyValue(fieldName);
					String strPropValue = propertyValue!=null ? propertyValue.toString() : null;
					field.value(strPropValue);
				}
			}
			
			if (boundPropertyCount>0) {
			// we bound some properties, this means it worth validating the bean
				Set<ConstraintViolation<?>> constraintViolations = validate(validator, object, properties);
				for (ConstraintViolation<?> constraintViolation: constraintViolations) {
					if (constraintViolation.getPropertyPath().toString().isEmpty()) {
						// Form error
						addError(constraintViolation.getMessage());
					} else {
						// Field error
						String fieldName = fieldPrefix!=null ? fieldPrefix + constraintViolation.getPropertyPath().toString() : constraintViolation.getPropertyPath().toString();
						Field field = field(fieldName);
						if (field!=null) {
							field.addError(constraintViolation.getMessage());
						}
					}
				}
			}
		}
		return this;
	}

	/**
	 * return a copy of the form
	 * 
	 */
	@Override
	public Form<T> clone() {
		// make a copy of the form
		Form<T> result = new Form<T>();
		for (Field field: fields()) {
			result.addField(field.clone());
		}
		return result;
	}

	/**
	 * remove all fields whose name is not contained in the specified properties
	 * 
	 * @param properties
	 * @return
	 */
	public Form<T> filter(String... properties) {
		Iterator<Entry<String, Field>> entries = fields.entrySet().iterator();
		while (entries.hasNext()) {
			Entry<String, Field> entry = entries.next();
			boolean foundKey = false;
			for (String property: properties) {
				foundKey = property!=null && property.equalsIgnoreCase(entry.getKey());
				if (foundKey) {
					break;
				}
			}
			// if we didn't find the property, remove it from the set
			if (!foundKey) {
				entries.remove();
			}
		}
		return this;
	}
	
	public T getObject() {
		return object;
	}

    /**
     * Creates a Bean of the given class type from the form values
     * @param cls class of the result bean
     * @param <R> type of the bean
     * @return
     */
    public <R> R deriveBean(Class<R> cls) {

        R result = null;
        try {
            result = cls.newInstance();
        } 
        catch (InstantiationException|IllegalAccessException e) {
            logger.log(Level.WARNING, "Could not instantiate derived bean", e);
            return null;
        }

        for (Field field: fields()) {
            // if applicable, remove the fieldPrefix to get the bean field name
            String fieldName = fieldPrefix!=null ? field.name().substring(fieldPrefix.length()) : field.name();
            BeanWrapper beanWrapper = new BeanWrapperImpl(result);
            try {
                PropertyDescriptor property = beanWrapper.getPropertyDescriptor(fieldName);
                if (property!=null && property.getReadMethod()!=null) {
                    beanWrapper.setPropertyValue(fieldName, field.value());
                }
            } catch(InvalidPropertyException ex) {
                logger.log(Level.WARNING, "Form property not in bean");
            }

        }

        return result;
    }

    /**
     * Creates a Bean with the given class from the from values
     * @param object Source object we're taking the values from
     * @return this object
     */
    public Form<T> fillFromBean(Object object) {

        BeanWrapper beanWrapper = new BeanWrapperImpl(object);
        for (Field field: fields()) {
            // if applicable, remove the fieldPrefix to get the bean field name
            String fieldName = fieldPrefix!=null ? field.name().substring(fieldPrefix.length()) : field.name();
            try {
                PropertyDescriptor property = beanWrapper.getPropertyDescriptor(fieldName);
                if (property!=null && property.getReadMethod()!=null) {
                    Object propertyValue = beanWrapper.getPropertyValue(fieldName);
                    String strPropValue = propertyValue!=null ? propertyValue.toString() : null;

                    // If the field hasn't got a value already, clear the potential errors as they will be meaningless
                    // and the set the field value
                    if (field.value() == null) {
                        field.value(strPropValue);
                        field.errors().clear();
                    }
                }
            } 
            catch(InvalidPropertyException e) {
                logger.log(Level.WARNING, "Could not find property " + fieldName + " for bean " + object.getClass().getName(), e);
                continue;
            }

        }
        return this;
    }

    /**
     * Overwrites the values of the bean in parameter with the form values
     * @param source
     * @return
     */
    public <R> void mergeInto(R source) {

        for (Field field: fields()) {
            // if applicable, remove the fieldPrefix to get the bean field name
            String fieldName = fieldPrefix!=null ? field.name().substring(fieldPrefix.length()) : field.name();
            BeanWrapper beanWrapper = new BeanWrapperImpl(source);
            try {
                PropertyDescriptor property = beanWrapper.getPropertyDescriptor(fieldName);
                if (property!=null && property.getReadMethod()!=null) {
                    beanWrapper.setPropertyValue(fieldName, field.value());
                }
            } 
            catch(InvalidPropertyException ex) {
                logger.log(Level.WARNING, "Form property not in source");
            }
        }
    }

	public boolean hasErrors() {
		if (errors != null && errors.size() > 0) {
			return true;
		}
		
		if (fields!=null) {
			for (Field field: fields.values()) {
				if (field.hasErrors()) {
					return true;
				}
			}
		}
		return false;
	}

	public Field field(String fieldName) {
		if (fields!=null) {
			return fields.get(fieldName);
		}
		return null;
	}

	public Iterable<Field> fields() {
		if (fields!=null) {
			return fields.values();
		}
		return Collections.emptyList();
	}

	public boolean isReadonly() {
		return readonly;
	}

	public String referer() {
		return referer;
	}

    // should not be accessible from the outside to cleanly encapsulate and prevent mistakes
	public Form<T> referer(String referer) {
		this.referer = referer;
		return this;
	}

	public String url() {
		return url;
	}

	public Form<T> url(String url) {
		this.url = url;
		return this;
	}
	
	public Form<T> setReadonly(boolean readonly) {
		this.readonly = readonly;
		return this;
	}

	public final static String fieldTypefromClass(Class<?> cls) {
		Objects.requireNonNull(cls);
	
		// by default return TEXT
		String result = "text";
		if (cls.equals(Boolean.class) || cls.equals(boolean.class)) {
			result = "checkbox";
		}
		else if (Number.class.isAssignableFrom(cls)) {
			result = "number";
		} 
		else if (cls.isEnum()) {
			result = "select";
		}
		return result;
	}

}
